#!/usr/bin/env node
import sharp from "sharp";
import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const SRC_DIR = path.join(ROOT, "public", "aquarelles");
const OUT_DIR = path.join(SRC_DIR, "optimized");
const MANIFEST = path.join(ROOT, "src", "data", "aquarelles.ts");

const WIDTHS = [200, 400, 800];
const GHOST = { opacity: 0.2, saturation: 0.55 };

const MAP = {
  "IMG_1461.png": { slug: "abeille-fleur",       type: "composition" },
  "IMG_1462.png": { slug: "dahlia",              type: "fleur" },
  "IMG_1464.png": { slug: "bouquet-coccinelle",  type: "composition" },
  "IMG_1465.png": { slug: "cocktail-orange",     type: "cocktail" },
  "IMG_1466.png": { slug: "abeille",             type: "insecte" },
  "IMG_1466 (1).png": { slug: "coccinelle",      type: "insecte" },
  "IMG_1468.png": { slug: "cocktail-grenade",    type: "cocktail" },
  "IMG_1469.png": { slug: "cocktail-citron",     type: "cocktail" },
};

await fs.mkdir(OUT_DIR, { recursive: true });

const entries = [];

for (const [filename, meta] of Object.entries(MAP)) {
  const inPath = path.join(SRC_DIR, filename);
  try { await fs.access(inPath); } catch { console.warn(`! Skip (missing): ${filename}`); continue; }

  const input = sharp(inPath);
  const { width: origW, height: origH } = await input.metadata();
  console.log(`→ ${filename} (${meta.slug}, ${origW}x${origH})`);

  for (const w of WIDTHS) {
    if (w > origW) continue;

    const base = sharp(inPath).resize({ width: w, withoutEnlargement: true });

    await base.clone().webp({ quality: 82, effort: 5 })
      .toFile(path.join(OUT_DIR, `${meta.slug}-${w}.webp`));
    await base.clone().png({ compressionLevel: 9, palette: true })
      .toFile(path.join(OUT_DIR, `${meta.slug}-${w}.png`));

    const ghost = sharp(inPath)
      .resize({ width: w, withoutEnlargement: true })
      .modulate({ saturation: GHOST.saturation })
      .ensureAlpha()
      .composite([{
        input: Buffer.from([255, 255, 255, Math.round(255 * (1 - GHOST.opacity))]),
        raw: { width: 1, height: 1, channels: 4 },
        tile: true,
        blend: "dest-in",
      }]);

    await ghost.clone().webp({ quality: 80, effort: 5 })
      .toFile(path.join(OUT_DIR, `${meta.slug}-ghost-${w}.webp`));
    await ghost.clone().png({ compressionLevel: 9 })
      .toFile(path.join(OUT_DIR, `${meta.slug}-ghost-${w}.png`));
  }

  entries.push({
    id: meta.slug,
    type: meta.type,
    width: origW,
    height: origH,
  });
}

const tsBody = `// AUTO-GENERATED by scripts/optimize-aquarelles.mjs — do not edit by hand.
export type AquarelleType = "insecte" | "fleur" | "cocktail" | "composition";

export interface Aquarelle {
  id: string;
  type: AquarelleType;
  width: number;
  height: number;
  src: string;         // default WebP (800w or largest)
  srcPng: string;      // PNG fallback
  srcGhost: string;    // ghost WebP
  srcGhostPng: string; // ghost PNG fallback
  srcSet: string;      // responsive WebP srcset
  srcSetGhost: string; // responsive WebP srcset (ghost)
}

const W = ${JSON.stringify(WIDTHS)} as const;

function build(id: string, type: AquarelleType, width: number, height: number): Aquarelle {
  const base = \`/aquarelles/optimized/\${id}\`;
  const widths = W.filter((w) => w <= width);
  const top = widths[widths.length - 1] ?? width;
  const set = (suffix: string) =>
    widths.map((w) => \`\${base}\${suffix}-\${w}.webp \${w}w\`).join(", ");
  return {
    id, type, width, height,
    src: \`\${base}-\${top}.webp\`,
    srcPng: \`\${base}-\${top}.png\`,
    srcGhost: \`\${base}-ghost-\${top}.webp\`,
    srcGhostPng: \`\${base}-ghost-\${top}.png\`,
    srcSet: set(""),
    srcSetGhost: set("-ghost"),
  };
}

export const AQUARELLES: Aquarelle[] = [
${entries.map((e) =>
  `  build(${JSON.stringify(e.id)}, ${JSON.stringify(e.type)}, ${e.width}, ${e.height}),`
).join("\n")}
];

export const byType = (t: AquarelleType) => AQUARELLES.filter((a) => a.type === t);
`;

await fs.writeFile(MANIFEST, tsBody);
console.log(`\n✓ ${entries.length} aquarelles optimisées`);
console.log(`✓ Manifeste écrit : ${path.relative(ROOT, MANIFEST)}`);
